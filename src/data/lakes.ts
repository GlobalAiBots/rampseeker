export function getLakeForRamp(lat: number, lng: number): Lake | undefined {
  for (const l of lakes) {
    if (Math.abs(lat - l.lat) < l.radius && Math.abs(lng - l.lng) < l.radius) {
      return l;
    }
  }
  return undefined;
}
